package reassigngreeting;

/**
 *
 * @author kcb
 */
public class ReassignGreeting 
{
    public static void main(String[] args) 
    {
        // Declare variables.
        String greeting = "Hello, person!";
        System.out.println(greeting);
        
        // Change value of variable.
        greeting = "Hello, Nina!";
        System.out.println(greeting);
    }  
}
